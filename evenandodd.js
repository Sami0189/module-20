//even

function isEven(number){
    if(number%2===0){
     return true;
    }
    else{
     return false;
    }
 }
 console.log(isEven(5));
 console.log(isEven(234));


//odd
 function isOdd(number){
    if(number%2===1){
        return true;
    }
    else{
        return false;
    }
 } 
 console.log(isOdd(534));
 console.log(isOdd(53));